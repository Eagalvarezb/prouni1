    private void cargarEmpleado() {
        modeloTabla.setRowCount(0);
        for (Empleado emp : controlador.obtenerEmpleado()) {
            modeloTabla.addRow(new Object[]{emp.getId(), emp.getNombre(), emp.getApellido(), emp.getPuesto(),emp.getTelefono(),emp.getDireccion(), emp.getEstado(),emp.getFecha_in(),emp.getSalario(),emp.getEmail()});
        }
    }
