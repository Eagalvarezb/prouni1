    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         try {
        if (txtFechaInicio.getText().isEmpty() || txtFechaFin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar ambas fechas", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Date fechaInicio = parseFecha(txtFechaInicio.getText());
        Date fechaFin = parseFecha(txtFechaFin.getText());

        // Obtener datos extendidos
        Map<String, Object> resumen = controlador.generarResumenVentas(fechaInicio, fechaFin);
        
        // Mostrar en tabla
        mostrarEnTabla((List<Reportes>) resumen.get("ventas"));
        
        // Mostrar informaci칩n adicional
        String mensaje = "Resumen de ventas:\n" +
                         "Total en ventas: Q" + resumen.get("totalVentas") + "\n" +
                         "Total productos vendidos: " + resumen.get("totalProductos") + "\n" +
                         "Usuarios con ventas: " + String.join(", ", (List<String>)resumen.get("usuariosActivos"));
        
        JOptionPane.showMessageDialog(this, mensaje, "Resumen", JOptionPane.INFORMATION_MESSAGE);
        
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use dd/mm/yyyy", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al buscar reportes: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
// TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

