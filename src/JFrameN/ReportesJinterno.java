    private void mostrarEnTabla(List<Reportes> datos) {
        modeloTabla.setRowCount(0);
        for (Reportes rep : datos) {
            modeloTabla.addRow(new Object[]{
                rep.getFecha(),
                rep.getUsuario(),
                rep.getProducto(),
                rep.getCantidad(),
                rep.getTotal()
            });
        }
        
    }
