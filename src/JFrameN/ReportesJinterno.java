    private void btnExportarPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarPDFActionPerformed
    try {
            if (txtFechaInicio.getText().isEmpty() || txtFechaFin.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar ambas fechas para exportar", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Date fechaInicio = parseFecha(txtFechaInicio.getText());
            Date fechaFin = parseFecha(txtFechaFin.getText());
            
             if (fechaInicio.after(fechaFin)) {
            JOptionPane.showMessageDialog(this, "La fecha de inicio debe ser anterior a la fecha fin", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

            List<Reportes> datos = controlador.obtenerPorFecha(fechaInicio, fechaFin);
            
            if (datos.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay datos para el rango de fechas seleccionado", "Informaci칩n", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        
            String nombreArchivo = "reporte_ventas_" + new SimpleDateFormat("yyyyMMdd").format(new Date()) + ".pdf";
          controlador.exportarPDF(datos, nombreArchivo, "Reporte de Ventas", fechaInicio, fechaFin);
        
            JOptionPane.showMessageDialog(this, "Reporte exportado a PDF: " + nombreArchivo, "칄xito", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use dd/mm/yyyy", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al exportar a PDF: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    // TODO add your handling code here:
    }//GEN-LAST:event_btnExportarPDFActionPerformed

